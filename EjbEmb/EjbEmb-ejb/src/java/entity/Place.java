package entity;

import javax.persistence.Embeddable;

/**
 *
 * @author Inspiron
 */
@Embeddable
public class Place {
    String town;
    String city;

    public String getTown() {
        return town;
    }

    public void setTown(String town) {
        this.town = town;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
    
}
